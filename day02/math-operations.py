# Addition +
mynum = 3 + 5
print("Addition : " + str(mynum))

# Subtraction -
mynum = 3 - 5
print("Subtraction : " + str(mynum))

# Multiplication *
mynum = 3 * 5
print("Multiplication : " + str(mynum))

# Division / ==> Always return floating type
mynum = 30 / 5
print("Division : " + str(mynum))

# to the Power **
mynum = 2 ** 6
print("to the Power : " + str(mynum))

# Rule of PEDMAS
# Parenthesis()
# exponential **
# division /
# multiplication *
# addition +
# subtraction -

print(3 * 3 + 3 / 3 - 3)

# How can we tell python to execute certain operation first to get 3.0?
print(3 * (3 + 3) / 3 - 3)
